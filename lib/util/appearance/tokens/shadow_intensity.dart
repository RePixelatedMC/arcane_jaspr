/// Shadow intensity presets for components.
///
/// Used by stylesheets to define shadow depth for different states.
enum ShadowIntensity {
  /// No shadow
  none,

  /// Subtle shadow - minimal elevation hint
  subtle,

  /// Standard shadow - default elevation
  standard,

  /// Elevated shadow - prominent elevation
  elevated,

  /// Dramatic shadow - heavy/dramatic effect
  dramatic,
}
