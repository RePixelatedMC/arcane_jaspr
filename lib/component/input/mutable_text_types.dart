/// Edit trigger mode for mutable text.
enum MutableTextTrigger {
  click,
  doubleClick,
  hover,
  always,
}

/// Input type for mutable text.
enum MutableTextInputType {
  text,
  multiline,
  number,
  email,
  url,
}

/// Display style when not editing.
enum MutableTextStyle {
  inline,
  subtle,
  underline,
  dashed,
  input,
}
